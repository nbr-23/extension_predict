-- Table: public.product

-- DROP TABLE IF EXISTS public.product;

CREATE TABLE IF NOT EXISTS public.product
(
    id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    date character varying(255) COLLATE pg_catalog."default",
    asin character varying(255) COLLATE pg_catalog."default",
    product_title character varying(300) COLLATE pg_catalog."default",
    revenue character varying(255) COLLATE pg_catalog."default",
    ordered_units character varying(255) COLLATE pg_catalog."default",
    glance_views character varying(255) COLLATE pg_catalog."default",
    conversion_rate character varying(255) COLLATE pg_catalog."default",
    CONSTRAINT product_pkey PRIMARY KEY (id)
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.product
    OWNER to postgres;


