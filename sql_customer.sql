-- Table: public.result

-- DROP TABLE IF EXISTS public.result;

CREATE TABLE IF NOT EXISTS public.result
(
    id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    id_advertising integer,
    id_product integer,
    prediction_id character varying(255) COLLATE pg_catalog."default",
    start_date character varying(255) COLLATE pg_catalog."default",
    end_date character varying(255) COLLATE pg_catalog."default",
    prediction character varying(255) COLLATE pg_catalog."default",
    CONSTRAINT result_pkey PRIMARY KEY (id),
    CONSTRAINT result_id_advertising_fkey FOREIGN KEY (id_advertising)
        REFERENCES public.advertising (id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT result_id_product_fkey FOREIGN KEY (id_product)
        REFERENCES public.product (id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.result
    OWNER to postgres;


